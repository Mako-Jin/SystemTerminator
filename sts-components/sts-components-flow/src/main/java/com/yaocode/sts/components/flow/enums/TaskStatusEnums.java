package com.yaocode.sts.components.flow.enums;

/**
 * 任务状态枚举
 * 参照Camunda工作流引擎的任务生命周期状态设计
 * 分为瞬时状态(Transient)和稳定状态(Stable)
 */
public enum TaskStatusEnums {

    // ==================== 瞬时状态 (Transient) ====================
    // 瞬时状态表示任务正在执行某个操作，是短暂的中间状态

    /**
     * 正在创建
     * 瞬时状态：任务正在被创建，尚未可用
     */
    CREATING,

    /**
     * 正在分配
     * 瞬时状态：正在为任务分配处理人
     */
    ASSIGNING,

    /**
     * 正在更新
     * 瞬时状态：任务信息（如处理人、截止时间）正在更新
     */
    UPDATING,

    /**
     * 正在完成
     * 瞬时状态：任务正在被完成，尚未结束
     */
    COMPLETING,

    /**
     * 正在取消
     * 瞬时状态：任务正在被取消
     */
    CANCELING,

    // ==================== 稳定状态 (Stable) ====================
    // 稳定状态表示任务处于某个稳定的状态，适合用于业务查询

    /**
     * 已创建
     * 稳定状态：任务已创建，等待被认领或处理。这是最常见的"待办"状态
     */
    CREATED,

    /**
     * 已认领
     * 稳定状态：任务已被用户认领，正在处理中
     */
    CLAIMED,

    /**
     * 已完成
     * 稳定状态：任务已成功完成
     */
    COMPLETED,

    /**
     * 已取消
     * 稳定状态：任务被取消（如流程被终止）
     */
    CANCELED,

    /**
     * 执行失败
     * 稳定状态：任务执行失败（如服务调用失败、脚本执行异常等）
     */
    FAILED,

    /**
     * 已过期
     * 稳定状态：任务超过截止时间未处理
     */
    EXPIRED,

}